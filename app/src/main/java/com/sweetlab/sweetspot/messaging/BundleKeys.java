package com.sweetlab.sweetspot.messaging;

public class BundleKeys {
    /**
     * Use when passing photo meta data in bundle.
     */
    public static final String PHOTO_META_KEY = "photo_meta_key";
    /**
     * Used when passing bitmap in bundle.
     */
    public static final String BITMAP_KEY = "bitmap_key";

    /**
     * Used to configure the collection fragment span count.
     */
    public static final String RECYCLER_VIEW_SPAN_COUNT = "recycler_view_span_count";

    /**
     * Used for configure the collection fragment orientation.
     */
    public static final String RECYCLER_VIEW_ORIENTATION = "recycler_view_orientation";

    /**
     * Used for configure the collection loader with modifier.
     */
    public static final String COLLECTION_LOADER_MODIFIER = "collection_loader_modifier";

    /**
     * Used to control if picasso should use a placeholder when loading images.
     */
    public static final String USE_PLACE_HOLDER = "use_place_holder";

    /**
     * Used to control if picasso should use a fade when loading images.
     */
    public static final String USE_FADE = "use_fade";

    /**
     * Used to tell which loader id to use.
     */
    public static final String LOADER_ID = "loader_id";
}
